    private void load_data()
    {
        Connection kon=Koneksi.connection();
        Object header[]={"NIK","NAMA","JKEL","TELEPON","ALAMAT","USERNAME","PASSWORD"};
        DefaultTableModel data= new DefaultTableModel(null, header);
        TabelUser.setModel(data);
        String sql_data="SELECT * FROM tb_user";
    try
    {
        Statement st=kon.createStatement();
        ResultSet rs=st.executeQuery(sql_data);
        while (rs.next())
        {
            String d1=rs.getString(1);
            String d2=rs.getString(2);
            String d3=rs.getString(3);
            String d4=rs.getString(4);
            String d5=rs.getString(5);
            String d6=rs.getString(6);
            String d7=rs.getString(7);
            
            String d[]={d1,d2,d3,d4,d5,d6,d7};
            data.addRow(d);
        }
    }
    catch(Exception e)
            {
            JOptionPane.showConfirmDialog(null, e);
            }
    }

