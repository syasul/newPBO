    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        String jk ="";
        try{
            String idUser = txtId.getText();
            String nama = txtNama.getText();
            String telepon = txtTelp.getText();
            String alamat = jTextArea1.getText();
            String username = txtUser.getText();
            String password = jPass.getText();
            //            String status = DropdownStatus.getSelectedItem().toString();

            if ("".equals(txtId.getText()) || "".equals(txtNama.getText()) || "".equals(txtTelp.getText()) || "".equals(jTextArea1.getText()) || "".equals(txtUser.getText()) || "".equals(jPass.getText())) {
                JOptionPane.showMessageDialog(this, "Harap Lengkapi Data", "Error", JOptionPane.WARNING_MESSAGE);
            } else {
                jk = "";
                if (rblk.isSelected()) {
                    jk = "Laki - Laki";
                } else {
                    jk = "Perempuan";
                }

                pst = kon.prepareStatement("UPDATE tb_user SET nama = ?, jkel= ?, telepon = ?, alamat = ?, username = ?, password = ? WHERE nik = ?");

                pst.setString(1, nama);
                pst.setString(2, jk);
                pst.setString(3, telepon);
                pst.setString(4, alamat);
                pst.setString(5, username);
                pst.setString(6, password);
                pst.setString(7, idUser);

                int k = pst.executeUpdate();
                if (k == 1) {
                    JOptionPane.showMessageDialog(this, "Data Berhasil diedit", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                    load_data();
                }else {
                    JOptionPane.showMessageDialog(this, "Data gagal diedit", "Sukses", JOptionPane.WARNING_MESSAGE);
                }
            }
        } catch(Exception e){
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_btnEditActionPerformed

